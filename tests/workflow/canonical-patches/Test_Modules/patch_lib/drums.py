"""ZOIA Patch Library — drums category."""
from patch_lib import PB, V, add_test_pages

def gen_drums(d):
    # K01 Kick Drum (Pushbutton on page 0)
    p=PB("Kick Drum"); bt=p.add(15,"Kick Pad",13); o=p.add(2,"Output",5)
    oc=p.add(14,"Kick Osc",2,par=[V(.08)]); ad=p.add(6,"Kick Env",1,par=[V(.005),V(.20),V(.0),V(.10)]); vc=p.add(7,"Kick VCA",2,par=[65535])
    p.labels("drums", "kick", "percussion", "interactive")
    p.desc("Synthesized kick drum triggered by pushbutton on the main page. Oscillator with pitch sweep and amplitude envelope. Press the button to trigger kicks.")
    p.c(bt,0,ad,0); p.c(oc,1,vc,0); p.c(ad,5,vc,1); p.c(ad,5,oc,0,5000); p.c(vc,2,o,0); p.c(vc,2,o,1); add_test_pages(p); p.save(f"{d}/K01_Kick_Drum.json")

    # K02 4-Voice Drum Kit (4 Pushbuttons on page 0, DSP on page 1+)
    p=PB("4-Voice Drum Kit",["Main","Kick+Snare","HiHat+Clap"])
    kp=p.add(15,"Kick",13); sp2=p.add(15,"Snare",13); hp=p.add(15,"HiHat",13); cp=p.add(15,"Clap",13); o=p.add(2,"Output",5)
    # Kick voice (page 1)
    ko=p.add(14,"Kick Osc",2,pg=1); ke=p.add(6,"Kick Env",1,pg=1); kv=p.add(7,"Kick VCA",2,pg=1)
    # Snare voice (page 1)
    sn=p.add(38,"Snr Noise",2,pg=1); sf=p.add(0,"Snr Filt",8,pg=1,par=[V(.55),V(.40)]); se=p.add(6,"Snr Env",1,pg=1,par=[V(.003),V(.12),V(.0),V(.08)]); sv=p.add(7,"Snr VCA",2,pg=1)
    # HiHat voice (page 2)
    hn=p.add(38,"HH Noise",2,pg=2); hf=p.add(0,"HH Filt",8,pg=2,par=[V(.80),V(.30)]); he=p.add(6,"HH Env",1,pg=2,par=[V(.001),V(.05),V(.0),V(.03)]); hv=p.add(7,"HH VCA",2,pg=2)
    # Clap voice (page 2)
    cn=p.add(38,"Clp Noise",2,pg=2); cf2=p.add(0,"Clp Filt",8,pg=2,par=[V(.60),V(.35)]); ce=p.add(6,"Clp Env",1,pg=2,par=[V(.005),V(.15),V(.0),V(.12)]); cv2=p.add(7,"Clp VCA",2,pg=2)
    # Mixer (page 2)
    mx=p.add(76,"Drum Mix",15,pg=2,par=[V(.80),V(.70),V(.65)])
    # Connections
    p.c(kp,0,ke,0); p.c(ko,1,kv,0); p.c(ke,5,kv,1); p.c(ke,5,ko,0,5000)
    p.c(sp2,0,se,0); p.c(sn,0,sf,0); p.c(sf,4,sv,0); p.c(se,5,sv,1)
    p.c(hp,0,he,0); p.c(hn,0,hf,0); p.c(hf,5,hv,0); p.c(he,5,hv,1)
    p.c(cp,0,ce,0); p.c(cn,0,cf2,0); p.c(cf2,4,cv2,0); p.c(ce,5,cv2,1)
    p.c(kv,2,mx,0); p.c(sv,2,mx,2); p.c(hv,2,mx,4); p.c(mx,6,o,0); p.c(mx,6,o,1); p.c(cv2,2,o,0,7000)
    p.labels("drums", "kit", "4-voice", "interactive", "midi")
    p.desc("Four-voice drum kit with kick, snare, hi-hat, and clap. Each has a pushbutton on the main page. Press buttons to trigger individual drum sounds.")
    add_test_pages(p); p.save(f"{d}/K02_4Voice_Drum_Kit.json")

    # K03 Minimal Techno (Stompswitch on page 0, DSP on page 1)
    p=PB("Minimal Techno",["Controls","Voices"])
    st=p.add(44,"Start/Stop",13); o=p.add(2,"Output",5)
    sq=p.add(4,"Beat Seq",1,pg=1,par=[V(1.0),V(.0),V(.0),V(.0),V(.5),V(.0),V(.0),V(.3)]); lf=p.add(5,"Clock",1,pg=1,par=[V(.25)]); ko=p.add(14,"Kick",2,pg=1,par=[V(.08)]); ke=p.add(6,"K Env",1,pg=1,par=[V(.005),V(.18),V(.0),V(.08)]); kv=p.add(7,"K VCA",2,pg=1,par=[65535]); hn=p.add(38,"HH",2,pg=1); hf=p.add(0,"HH Filt",8,pg=1,par=[V(.80),V(.25)]); he=p.add(6,"HH Env",1,pg=1,par=[V(.001),V(.04),V(.0),V(.02)]); hv=p.add(7,"HH VCA",2,pg=1,par=[65535]); cd=p.add(49,"HH Clock",1,pg=1,par=[V(.5)]); mx=p.add(76,"Mix",15,pg=1,par=[V(.80),V(.60),V(.0)])
    p.c(lf,1,sq,0); p.c(lf,1,cd,0); p.c(sq,11,ke,0); p.c(cd,2,he,0); p.c(ko,1,kv,0); p.c(ke,5,kv,1); p.c(ke,5,ko,0,5000); p.c(hn,0,hf,0); p.c(hf,5,hv,0); p.c(he,5,hv,1); p.c(kv,2,mx,0); p.c(hv,2,mx,2); p.c(mx,6,o,0); p.c(mx,6,o,1)
    p.labels("drums", "techno", "sequencer", "interactive")
    p.desc("Self-sequencing techno drum machine with kick and hi-hat. Stompswitch on main page starts and stops the pattern. Adjust sound parameters on DSP page.")
    add_test_pages(p); p.save(f"{d}/K03_Minimal_Techno.json")

    # K04 Industrial Drums (Pushbuttons on page 0)
    p=PB("Industrial Drums"); b1=p.add(15,"Hit 1",13); b2=p.add(15,"Hit 2",13); o=p.add(2,"Output",5)
    oc=p.add(14,"Metal Osc",2,par=[V(.12)]); e1=p.add(6,"Env 1",1,par=[V(.005),V(.25),V(.0),V(.15)]); fz=p.add(66,"Destroy",3,par=[0,V(.90),V(.30),0]); v1=p.add(7,"VCA 1",2,par=[65535]); ns=p.add(38,"Noise",2); e2=p.add(6,"Env 2",1,par=[V(.005),V(.15),V(.0),V(.08)]); ds=p.add(11,"Crunch",3,par=[0,V(.85),V(.35),0]); al=p.add(3,"Digital",12,par=[0,V(.55),0]); v2=p.add(7,"VCA 2",2,par=[65535]); mx=p.add(76,"Mix",15,par=[V(.75),V(.70),V(.0)])
    p.c(b1,0,e1,0); p.c(oc,1,fz,0); p.c(fz,3,v1,0); p.c(e1,5,v1,1); p.c(e1,5,oc,0,4000); p.c(b2,0,e2,0); p.c(ns,0,ds,0); p.c(ds,3,al,0); p.c(al,2,v2,0); p.c(e2,5,v2,1); p.c(v1,2,mx,0); p.c(v2,2,mx,2); p.c(mx,6,o,0); p.c(mx,6,o,1)
    p.labels("drums", "industrial", "noise", "interactive")
    p.desc("Industrial percussion using noise and ring modulation with pushbuttons. Harsh, metallic drum sounds. Press buttons on main page to trigger hits.")
    add_test_pages(p); p.save(f"{d}/K04_Industrial_Drums.json")

    # K05 Ambient Percussion (Pushbutton on page 0)
    p=PB("Ambient Percussion"); bt=p.add(15,"Trigger",13); o=p.add(2,"Output",5)
    ns=p.add(38,"Source",2); rg=p.add(42,"Metallic",12); oc=p.add(14,"Bell Osc",2,par=[V(.70)]); en=p.add(6,"Shape",1,par=[V(.02),V(.50),V(.20),V(.80)]); vc=p.add(7,"VCA",2,par=[65535]); sh=p.add(27,"Shimmer",7,par=[0,V(.80),V(.55),V(.60),0])
    p.labels("drums", "ambient", "percussion", "interactive")
    p.desc("Ambient percussion with ring-modulated noise into shimmer reverb. Pushbutton triggers ethereal metallic hits. Reverb creates long, evolving tails.")
    p.c(bt,0,en,0); p.c(ns,0,rg,0); p.c(oc,1,rg,1); p.c(rg,2,vc,0); p.c(en,5,vc,1); p.c(vc,2,sh,0); p.c(sh,4,o,0); p.c(sh,4,o,1); add_test_pages(p); p.save(f"{d}/K05_Ambient_Percussion.json")

    # K06 808 Kick (Pushbutton on page 0)
    p=PB("808 Kick"); bt=p.add(15,"Kick Pad",13); o=p.add(2,"Output",5)
    oc=p.add(14,"Sine Osc",2,par=[V(.06)]); ad=p.add(6,"Pitch Env",1,par=[V(.001),V(.025),V(.0),V(.005)]); a2=p.add(6,"Amp Env",1,par=[V(.001),V(.60),V(.0),V(.15)]); vc=p.add(7,"808 VCA",2,par=[65535])
    p.labels("drums", "808", "kick", "interactive", "bass")
    p.desc("Classic 808 kick drum synthesis with long pitch sweep and sustain. Pushbutton on main page triggers. Dual envelopes shape pitch and amplitude independently.")
    p.c(bt,0,ad,0); p.c(bt,0,a2,0); p.c(oc,1,vc,0); p.c(ad,5,oc,0,6000); p.c(a2,5,vc,1); p.c(vc,2,o,0); p.c(vc,2,o,1); add_test_pages(p); p.save(f"{d}/K06_808_Kick.json")

    # K07 909 Kick (Pushbutton on page 0)
    p=PB("909 Kick"); bt=p.add(15,"Kick Pad",13); o=p.add(2,"Output",5)
    oc=p.add(14,"Sine Osc",2,par=[V(.10)]); ns=p.add(38,"Click",2); ad=p.add(6,"Pitch Env",1,par=[V(.001),V(.015),V(.0),V(.005)]); a2=p.add(6,"Amp Env",1,par=[V(.001),V(.25),V(.0),V(.08)]); vc=p.add(7,"909 VCA",2,par=[65535]); mx=p.add(76,"Mix",15,par=[V(.85),V(.20),V(.00)])
    p.labels("drums", "909", "kick", "interactive")
    p.desc("909-style kick drum blending oscillator with noise burst. Pushbutton on main page triggers. Punchier attack than the 808, with added click from noise.")
    p.c(bt,0,ad,0); p.c(bt,0,a2,0); p.c(oc,1,mx,0); p.c(ns,0,mx,2); p.c(ad,5,oc,0,5000); p.c(mx,6,vc,0); p.c(a2,5,vc,1); p.c(vc,2,o,0); p.c(vc,2,o,1); add_test_pages(p); p.save(f"{d}/K07_909_Kick.json")
